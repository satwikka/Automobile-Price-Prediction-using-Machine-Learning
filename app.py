    "import streamlit as st\n",
    "import pandas as pd\n",
    "import numpy as np\n",
    "import matplotlib.pyplot as plt\n",
    "import seaborn as sns\n",
    "\n",
    "from sklearn.model_selection import train_test_split\n",
    "from sklearn.preprocessing import LabelEncoder\n",
    "from sklearn.linear_model import LinearRegression, Ridge, Lasso\n",
    "from sklearn.ensemble import RandomForestRegressor\n",
    "from sklearn.metrics import r2_score, mean_absolute_error, mean_squared_error\n",
    "\n",
    "# ---------------- TITLE ----------------\n",
    "st.title(\"Automobile Price Prediction Dashboard\")\n",
    "st.write(\"EDA + Visualizations + Regression Models\")\n",
    "\n",
    "# ---------------- LOAD DATA ----------------\n",
    "df = pd.read_csv(\"automobile.csv\")\n",
    "df.replace(\"?\", np.nan, inplace=True)\n",
    "\n",
    "# Convert numeric columns\n",
    "for col in df.columns:\n",
    "    df[col] = pd.to_numeric(df[col], errors=\"ignore\")\n",
    "\n",
    "df.fillna(df.median(numeric_only=True), inplace=True)\n",
    "\n",
    "st.subheader(\"Dataset Preview\")\n",
    "st.dataframe(df.head())\n",
    "\n",
    "# ---------------- ENCODING ----------------\n",
    "le = LabelEncoder()\n",
    "for col in df.select_dtypes(include=\"object\").columns:\n",
    "    df[col] = le.fit_transform(df[col])\n",
    "\n",
    "# ---------------- VISUALIZATIONS ----------------\n",
    "st.subheader(\"Exploratory Data Analysis\")\n",
    "\n",
    "# 1. Price Distribution\n",
    "st.markdown(\"### Price Distribution\")\n",
    "fig1, ax1 = plt.subplots()\n",
    "ax1.hist(df[\"price\"], bins=30)\n",
    "st.pyplot(fig1)\n",
    "\n",
    "# 2. Correlation Heatmap\n",
    "st.markdown(\"### Correlation Heatmap\")\n",
    "fig2, ax2 = plt.subplots(figsize=(10, 6))\n",
    "sns.heatmap(df.corr(), cmap=\"coolwarm\", ax=ax2)\n",
    "st.pyplot(fig2)\n",
    "\n",
    "# 3. Engine Size vs Price\n",
    "st.markdown(\"### Engine Size vs Price\")\n",
    "fig3, ax3 = plt.subplots()\n",
    "ax3.scatter(df[\"engine-size\"], df[\"price\"])\n",
    "ax3.set_xlabel(\"Engine Size\")\n",
    "ax3.set_ylabel(\"Price\")\n",
    "st.pyplot(fig3)\n",
    "\n",
    "# 4. Horsepower vs Price\n",
    "st.markdown(\"### Horsepower vs Price\")\n",
    "fig4, ax4 = plt.subplots()\n",
    "ax4.scatter(df[\"horsepower\"], df[\"price\"])\n",
    "ax4.set_xlabel(\"Horsepower\")\n",
    "ax4.set_ylabel(\"Price\")\n",
    "st.pyplot(fig4)\n",
    "\n",
    "# 5. City MPG vs Price\n",
    "st.markdown(\"### City MPG vs Price\")\n",
    "fig5, ax5 = plt.subplots()\n",
    "ax5.scatter(df[\"city-mpg\"], df[\"price\"])\n",
    "ax5.set_xlabel(\"City MPG\")\n",
    "ax5.set_ylabel(\"Price\")\n",
    "st.pyplot(fig5)\n",
    "\n",
    "# ---------------- FEATURES & TARGET ----------------\n",
    "X = df.drop(\"price\", axis=1)\n",
    "y = df[\"price\"]\n",
    "\n",
    "# ---------------- TRAIN TEST SPLIT ----------------\n",
    "X_train, X_test, y_train, y_test = train_test_split(\n",
    "    X, y, test_size=0.2, random_state=42\n",
    ")\n",
    "\n",
    "# ---------------- MODELS ----------------\n",
    "models = {\n",
    "    \"Linear Regression\": LinearRegression(),\n",
    "    \"Ridge Regression\": Ridge(),\n",
    "    \"Lasso Regression\": Lasso(),\n",
    "    \"Random Forest\": RandomForestRegressor(random_state=42)\n",
    "}\n",
    "\n",
    "# ---------------- MODEL TRAINING ----------------\n",
    "st.subheader(\"Model Performance\")\n",
    "\n",
    "results = []\n",
    "\n",
    "for name, model in models.items():\n",
    "    model.fit(X_train, y_train)\n",
    "    y_pred = model.predict(X_test)\n",
    "\n",
    "    results.append([\n",
    "        name,\n",
    "        r2_score(y_test, y_pred),\n",
    "        mean_absolute_error(y_test, y_pred),\n",
    "        mean_squared_error(y_test, y_pred)\n",
    "    ])\n",
    "\n",
    "results_df = pd.DataFrame(\n",
    "    results, columns=[\"Model\", \"R2 Score\", \"MAE\", \"MSE\"]\n",
    ")\n",
    "\n",
    "st.dataframe(results_df)\n",
